import UIKit

extension UIColor{
    class func getLightGrayBoderColor() ->UIColor{
        return UIColor(red: 221.0/255.0, green: 221.0/225.0, blue: 221.0/225.0, alpha: 1.0)
            }
    class func getMidGrayColor() -> UIColor{
        return UIColor(red: 188, green: 189, blue: 195, alpha: 1.0)
    }
}
